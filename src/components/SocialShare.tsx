"use client";

import React, { useState } from "react";
import { useTheme } from "@/providers/ThemeProvider";
import { useLanguage } from "@/providers/LanguageProvider";

interface SocialShareProps {
  url: string;
  title: string;
  description?: string;
  image?: string;
  hashtags?: string[];
  className?: string;
  size?: "sm" | "md" | "lg";
  showLabel?: boolean;
  variant?: "buttons" | "icons" | "dropdown";
}

const SocialShare: React.FC<SocialShareProps> = ({
  url,
  title,
  description = "",
  hashtags = [],
  className = "",
  size = "md",
  showLabel = true,
  variant = "buttons",
}) => {
  const { isDark } = useTheme();
  const { language } = useLanguage();
  const [showDropdown, setShowDropdown] = useState(false);
  const [copied, setCopied] = useState(false);

  const encodedUrl = encodeURIComponent(url);
  const encodedTitle = encodeURIComponent(title);
  const encodedDescription = encodeURIComponent(description);
  const hashtagString = hashtags.map((h) => h.replace("#", "")).join(",");

  const shareLinks = [
    {
      name: "Facebook",
      icon: (
        <svg className="w-full h-full" fill="currentColor" viewBox="0 0 24 24">
          <path d="M24 12.073c0-6.627-5.373-12-12-12s-12 5.373-12 12c0 5.99 4.388 10.954 10.125 11.854v-8.385H7.078v-3.47h3.047V9.43c0-3.007 1.792-4.669 4.533-4.669 1.312 0 2.686.235 2.686.235v2.953H15.83c-1.491 0-1.956.925-1.956 1.874v2.25h3.328l-.532 3.47h-2.796v8.385C19.612 23.027 24 18.062 24 12.073z" />
        </svg>
      ),
      url: `https://www.facebook.com/sharer/sharer.php?u=${encodedUrl}`,
      color: "bg-[#1877F2] hover:bg-[#166FE5]",
      textColor: "text-white",
    },
    {
      name: "Twitter",
      icon: (
        <svg className="w-full h-full" fill="currentColor" viewBox="0 0 24 24">
          <path d="M18.244 2.25h3.308l-7.227 8.26 8.502 11.24H16.17l-5.214-6.817L4.99 21.75H1.68l7.73-8.835L1.254 2.25H8.08l4.713 6.231zm-1.161 17.52h1.833L7.084 4.126H5.117z" />
        </svg>
      ),
      url: `https://twitter.com/intent/tweet?url=${encodedUrl}&text=${encodedTitle}${
        hashtagString ? `&hashtags=${hashtagString}` : ""
      }`,
      color: "bg-black hover:bg-gray-800",
      textColor: "text-white",
    },
    {
      name: "WhatsApp",
      icon: (
        <svg className="w-full h-full" fill="currentColor" viewBox="0 0 24 24">
          <path d="M17.472 14.382c-.297-.149-1.758-.867-2.03-.967-.273-.099-.471-.148-.67.15-.197.297-.767.966-.94 1.164-.173.199-.347.223-.644.075-.297-.15-1.255-.463-2.39-1.475-.883-.788-1.48-1.761-1.653-2.059-.173-.297-.018-.458.13-.606.134-.133.298-.347.446-.52.149-.174.198-.298.298-.497.099-.198.05-.371-.025-.52-.075-.149-.669-1.612-.916-2.207-.242-.579-.487-.5-.669-.51-.173-.008-.371-.01-.57-.01-.198 0-.52.074-.792.372-.272.297-1.04 1.016-1.04 2.479 0 1.462 1.065 2.875 1.213 3.074.149.198 2.096 3.2 5.077 4.487.709.306 1.262.489 1.694.625.712.227 1.36.195 1.871.118.571-.085 1.758-.719 2.006-1.413.248-.694.248-1.289.173-1.413-.074-.124-.272-.198-.57-.347m-5.421 7.403h-.004a9.87 9.87 0 01-5.031-1.378l-.361-.214-3.741.982.998-3.648-.235-.374a9.86 9.86 0 01-1.51-5.26c.001-5.45 4.436-9.884 9.888-9.884 2.64 0 5.122 1.03 6.988 2.898a9.825 9.825 0 012.893 6.994c-.003 5.45-4.437 9.884-9.885 9.884m8.413-18.297A11.815 11.815 0 0012.05 0C5.495 0 .16 5.335.157 11.892c0 2.096.547 4.142 1.588 5.945L.057 24l6.305-1.654a11.882 11.882 0 005.683 1.448h.005c6.554 0 11.89-5.335 11.893-11.893a11.821 11.821 0 00-3.48-8.413z" />
        </svg>
      ),
      url: `https://wa.me/?text=${encodedTitle}%20${encodedUrl}`,
      color: "bg-[#25D366] hover:bg-[#20BD5A]",
      textColor: "text-white",
    },
    {
      name: "LinkedIn",
      icon: (
        <svg className="w-full h-full" fill="currentColor" viewBox="0 0 24 24">
          <path d="M20.447 20.452h-3.554v-5.569c0-1.328-.027-3.037-1.852-3.037-1.853 0-2.136 1.445-2.136 2.939v5.667H9.351V9h3.414v1.561h.046c.477-.9 1.637-1.85 3.37-1.85 3.601 0 4.267 2.37 4.267 5.455v6.286zM5.337 7.433c-1.144 0-2.063-.926-2.063-2.065 0-1.138.92-2.063 2.063-2.063 1.14 0 2.064.925 2.064 2.063 0 1.139-.925 2.065-2.064 2.065zm1.782 13.019H3.555V9h3.564v11.452zM22.225 0H1.771C.792 0 0 .774 0 1.729v20.542C0 23.227.792 24 1.771 24h20.451C23.2 24 24 23.227 24 22.271V1.729C24 .774 23.2 0 22.222 0h.003z" />
        </svg>
      ),
      url: `https://www.linkedin.com/sharing/share-offsite/?url=${encodedUrl}`,
      color: "bg-[#0A66C2] hover:bg-[#004182]",
      textColor: "text-white",
    },
    {
      name: "Telegram",
      icon: (
        <svg className="w-full h-full" fill="currentColor" viewBox="0 0 24 24">
          <path d="M11.944 0A12 12 0 0 0 0 12a12 12 0 0 0 12 12 12 12 0 0 0 12-12A12 12 0 0 0 12 0a12 12 0 0 0-.056 0zm4.962 7.224c.1-.002.321.023.465.14a.506.506 0 0 1 .171.325c.016.093.036.306.02.472-.18 1.898-.962 6.502-1.36 8.627-.168.9-.499 1.201-.82 1.23-.696.065-1.225-.46-1.9-.902-1.056-.693-1.653-1.124-2.678-1.8-1.185-.78-.417-1.21.258-1.91.177-.184 3.247-2.977 3.307-3.23.007-.032.014-.15-.056-.212s-.174-.041-.249-.024c-.106.024-1.793 1.14-5.061 3.345-.48.33-.913.49-1.302.48-.428-.008-1.252-.241-1.865-.44-.752-.245-1.349-.374-1.297-.789.027-.216.325-.437.893-.663 3.498-1.524 5.83-2.529 6.998-3.014 3.332-1.386 4.025-1.627 4.476-1.635z" />
        </svg>
      ),
      url: `https://t.me/share/url?url=${encodedUrl}&text=${encodedTitle}`,
      color: "bg-[#0088CC] hover:bg-[#006DA3]",
      textColor: "text-white",
    },
    {
      name: "Email",
      icon: (
        <svg className="w-full h-full" fill="none" stroke="currentColor" viewBox="0 0 24 24">
          <path
            strokeLinecap="round"
            strokeLinejoin="round"
            strokeWidth={2}
            d="M3 8l7.89 5.26a2 2 0 002.22 0L21 8M5 19h14a2 2 0 002-2V7a2 2 0 00-2-2H5a2 2 0 00-2 2v10a2 2 0 002 2z"
          />
        </svg>
      ),
      url: `mailto:?subject=${encodedTitle}&body=${encodedDescription}%0A%0A${encodedUrl}`,
      color: isDark ? "bg-gray-600 hover:bg-gray-500" : "bg-gray-500 hover:bg-gray-600",
      textColor: "text-white",
    },
  ];

  const sizeClasses = {
    sm: { button: "p-1.5", icon: "w-4 h-4 block", text: "text-xs" },
    md: { button: "p-2", icon: "w-5 h-5 block", text: "text-sm" },
    lg: { button: "p-3", icon: "w-6 h-6 block", text: "text-base" },
  };

  const copyToClipboard = async () => {
    try {
      await navigator.clipboard.writeText(url);
      setCopied(true);
      setTimeout(() => setCopied(false), 2000);
    } catch (err) {
      console.error("Failed to copy:", err);
    }
  };

  const handleShare = (shareUrl: string) => {
    window.open(shareUrl, "_blank", "width=600,height=400");
  };

  if (variant === "dropdown") {
    return (
      <div className={`relative ${className}`}>
        <button
          onClick={() => setShowDropdown(!showDropdown)}
          className={`flex items-center gap-2 ${sizeClasses[size].button} rounded-lg transition-all ${
            isDark
              ? "bg-gray-700 hover:bg-gray-600 text-white"
              : "bg-gray-100 hover:bg-gray-200 text-gray-700"
          }`}
        >
          <svg
            className={sizeClasses[size].icon}
            fill="none"
            stroke="currentColor"
            viewBox="0 0 24 24"
          >
            <path
              strokeLinecap="round"
              strokeLinejoin="round"
              strokeWidth={2}
              d="M8.684 13.342C8.886 12.938 9 12.482 9 12c0-.482-.114-.938-.316-1.342m0 2.684a3 3 0 110-2.684m0 2.684l6.632 3.316m-6.632-6l6.632-3.316m0 0a3 3 0 105.367-2.684 3 3 0 00-5.367 2.684zm0 9.316a3 3 0 105.368 2.684 3 3 0 00-5.368-2.684z"
            />
          </svg>
          {showLabel && (
            <span className={sizeClasses[size].text}>
              {language === "bn" ? "শেয়ার করুন" : "Share"}
            </span>
          )}
        </button>

        {showDropdown && (
          <>
            <div
              className="fixed inset-0 z-40"
              onClick={() => setShowDropdown(false)}
            />
            <div
              className={`absolute right-0 mt-2 w-48 rounded-xl shadow-xl z-50 py-2 ${
                isDark ? "bg-gray-800 border border-gray-700" : "bg-white border border-gray-200"
              }`}
            >
              {shareLinks.map((link) => (
                <button
                  key={link.name}
                  onClick={() => {
                    handleShare(link.url);
                    setShowDropdown(false);
                  }}
                  className={`w-full px-4 py-2 flex items-center gap-3 transition-colors ${
                    isDark ? "hover:bg-gray-700" : "hover:bg-gray-100"
                  }`}
                >
                  <span className={`${sizeClasses[size].icon} ${link.textColor}`}>
                    {link.icon}
                  </span>
                  <span className={isDark ? "text-gray-200" : "text-gray-700"}>
                    {link.name}
                  </span>
                </button>
              ))}
              <hr className={isDark ? "border-gray-700 my-2" : "border-gray-200 my-2"} />
              <button
                onClick={() => {
                  copyToClipboard();
                  setShowDropdown(false);
                }}
                className={`w-full px-4 py-2 flex items-center gap-3 transition-colors ${
                  isDark ? "hover:bg-gray-700" : "hover:bg-gray-100"
                }`}
              >
                <svg
                  className={sizeClasses[size].icon}
                  fill="none"
                  stroke="currentColor"
                  viewBox="0 0 24 24"
                >
                  <path
                    strokeLinecap="round"
                    strokeLinejoin="round"
                    strokeWidth={2}
                    d="M8 5H6a2 2 0 00-2 2v12a2 2 0 002 2h10a2 2 0 002-2v-1M8 5a2 2 0 002 2h2a2 2 0 002-2M8 5a2 2 0 012-2h2a2 2 0 012 2m0 0h2a2 2 0 012 2v3m2 4H10m0 0l3-3m-3 3l3 3"
                  />
                </svg>
                <span className={isDark ? "text-gray-200" : "text-gray-700"}>
                  {copied
                    ? language === "bn"
                      ? "কপি হয়েছে!"
                      : "Copied!"
                    : language === "bn"
                    ? "লিংক কপি করুন"
                    : "Copy Link"}
                </span>
              </button>
            </div>
          </>
        )}
      </div>
    );
  }

  if (variant === "icons") {
    return (
      <div className={`flex items-center gap-2 ${className}`}>
        {shareLinks.slice(0, 5).map((link) => (
          <button
            key={link.name}
            onClick={() => handleShare(link.url)}
            className={`${sizeClasses[size].button} rounded-full ${link.color} ${link.textColor} transition-all hover:scale-110 flex items-center justify-center`}
            title={link.name}
          >
            <span className={`${sizeClasses[size].icon} flex-shrink-0`}>{link.icon}</span>
          </button>
        ))}
        <button
          onClick={copyToClipboard}
          className={`${sizeClasses[size].button} rounded-full transition-all hover:scale-110 ${
            copied
              ? "bg-green-500 text-white"
              : isDark
              ? "bg-gray-700 hover:bg-gray-600 text-white"
              : "bg-gray-200 hover:bg-gray-300 text-gray-700"
          }`}
          title={copied ? "Copied!" : "Copy Link"}
        >
          {copied ? (
            <svg className={sizeClasses[size].icon} fill="currentColor" viewBox="0 0 20 20">
              <path
                fillRule="evenodd"
                d="M16.707 5.293a1 1 0 010 1.414l-8 8a1 1 0 01-1.414 0l-4-4a1 1 0 011.414-1.414L8 12.586l7.293-7.293a1 1 0 011.414 0z"
                clipRule="evenodd"
              />
            </svg>
          ) : (
            <svg
              className={sizeClasses[size].icon}
              fill="none"
              stroke="currentColor"
              viewBox="0 0 24 24"
            >
              <path
                strokeLinecap="round"
                strokeLinejoin="round"
                strokeWidth={2}
                d="M13.828 10.172a4 4 0 00-5.656 0l-4 4a4 4 0 105.656 5.656l1.102-1.101m-.758-4.899a4 4 0 005.656 0l4-4a4 4 0 00-5.656-5.656l-1.1 1.1"
              />
            </svg>
          )}
        </button>
      </div>
    );
  }

  // Default: buttons variant
  return (
    <div className={`flex flex-wrap items-center gap-2 ${className}`}>
      {shareLinks.map((link) => (
        <button
          key={link.name}
          onClick={() => handleShare(link.url)}
          className={`flex items-center gap-2 ${sizeClasses[size].button} px-3 rounded-lg ${link.color} ${link.textColor} transition-all hover:scale-105`}
        >
          <span className={sizeClasses[size].icon}>{link.icon}</span>
          {showLabel && <span className={sizeClasses[size].text}>{link.name}</span>}
        </button>
      ))}
      <button
        onClick={copyToClipboard}
        className={`flex items-center gap-2 ${sizeClasses[size].button} px-3 rounded-lg transition-all hover:scale-105 ${
          copied
            ? "bg-green-500 text-white"
            : isDark
            ? "bg-gray-700 hover:bg-gray-600 text-white"
            : "bg-gray-200 hover:bg-gray-300 text-gray-700"
        }`}
      >
        {copied ? (
          <>
            <svg className={sizeClasses[size].icon} fill="currentColor" viewBox="0 0 20 20">
              <path
                fillRule="evenodd"
                d="M16.707 5.293a1 1 0 010 1.414l-8 8a1 1 0 01-1.414 0l-4-4a1 1 0 011.414-1.414L8 12.586l7.293-7.293a1 1 0 011.414 0z"
                clipRule="evenodd"
              />
            </svg>
            {showLabel && (
              <span className={sizeClasses[size].text}>
                {language === "bn" ? "কপি হয়েছে!" : "Copied!"}
              </span>
            )}
          </>
        ) : (
          <>
            <svg
              className={sizeClasses[size].icon}
              fill="none"
              stroke="currentColor"
              viewBox="0 0 24 24"
            >
              <path
                strokeLinecap="round"
                strokeLinejoin="round"
                strokeWidth={2}
                d="M13.828 10.172a4 4 0 00-5.656 0l-4 4a4 4 0 105.656 5.656l1.102-1.101m-.758-4.899a4 4 0 005.656 0l4-4a4 4 0 00-5.656-5.656l-1.1 1.1"
              />
            </svg>
            {showLabel && (
              <span className={sizeClasses[size].text}>
                {language === "bn" ? "লিংক কপি" : "Copy Link"}
              </span>
            )}
          </>
        )}
      </button>
    </div>
  );
};

export default SocialShare;
